void solve() {
    int l, r;
    std::cin >> l >> r;

    auto check = [&](int x) -> bool {
        for (int i = 2; i * i <= x; i++) {
            if (x % i == 0) {
                return false;
            }
        }
        return true;
    };

    for (int i = 2; i < 100; i++) {
        for (int j = i + 1; j < 100; j++) {
            for (int k = j + 1; k < 100; k++) {
                if (check(i) && check(j) && check(k)) {
                    if (i * j * k >= l && i * j * k <= r) {
                        std::cout << i * j * k << "\n";
                        return ;
                    }
                }
            }
        }
    }

    std::cout << "-1\n";
}

int main() {
    IOS;
    int t = 1;
    // std::cin >> t;
    while (t -- )
        solve();
    return 0;
}
