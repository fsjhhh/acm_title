typedef long long LL;
typedef std::pair<int, int> PII;
typedef std::pair<LL, LL> PLL;
const int INF = 0x3f3f3f3f;
const LL INFL = 0x3f3f3f3f3f3f3f3f;

LL check(LL u)
{
	if (u == 0)
		return 0;
	if (u == 1)
		return 1;
	return check(u / 2) + u % 2;
}

void solve()
{
	LL n;
	std::cin >> n;
	LL x = check(n);
	std::cout << x << " " << (1LL << x) - 1 << "\n";
}

int main()
{
    IOS;
    int t = 1;
    std::cin >> t;
    while (t -- )
        solve();
    return 0;
}

