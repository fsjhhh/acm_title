typedef std::pair<LL, LL> PLL;
const int INF = 0x3f3f3f3f;
const LL INFL = 0x3f3f3f3f3f3f3f3f;

void solve() {
    LL A, M, L, R;
    std::cin >> A >> M >> L >> R;
    LL l, r;
    if (L - 1 < A) {
        l = (L - A - 1 - M + 1) / M;
    } else {
        l = (L - A - 1) / M;
    }
    if (R < A) {
        r = (R - A - M + 1) / M;
    } else {
        r = (R - A) / M;
    }
    std::cout << std::max((r - l), 0ll) << "\n";
}

int main() {
    IOS;
    int t = 1;
    // std::cin >> t;
    while (t -- )
        solve();
    return 0;
}
